#!/usr/bin/env node
/**
 * Build shared/src/airports.ts from OurAirports CSV dumps in data/.
 * Run: node scripts/generate-airports.mjs
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

/** ICAO codes for NYC area + world's 10 busiest airports (by passengers). */
const ICAOS = [
  "KJFK",
  "KEWR",
  "KLGA",
  "KATL",
  "OMDB",
  "KDFW",
  "EGLL",
  "LTFM",
  "KDEN",
  "WSSS",
  "KLAX",
  "KORD",
  "VIDP",
];

const GROUPS = [
  { label: "New York area", icaos: ["KJFK", "KEWR", "KLGA"] },
  {
    label: "Busiest in the world",
    icaos: ["KATL", "OMDB", "KDFW", "EGLL", "LTFM", "KDEN", "WSSS", "KLAX", "KORD", "VIDP"],
  },
];

function parseCsvLine(line) {
  const cols = [];
  let cur = "";
  let inQ = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (c === '"') {
      inQ = !inQ;
      continue;
    }
    if (c === "," && !inQ) {
      cols.push(cur);
      cur = "";
      continue;
    }
    cur += c;
  }
  cols.push(cur);
  return cols;
}

function readCsv(path) {
  const [headerLine, ...rows] = readFileSync(path, "utf8").trim().split("\n");
  const headers = parseCsvLine(headerLine).map((h) => h.replace(/^"|"$/g, ""));
  return rows.map((line) => {
    const cols = parseCsvLine(line);
    return Object.fromEntries(headers.map((h, i) => [h, cols[i] ?? ""]));
  });
}

function round6(n) {
  return Math.round(n * 1e6) / 1e6;
}

const airports = {};
for (const row of readCsv(resolve(root, "data/airports.csv"))) {
  const icao = row.icao_code || row.gps_code || row.ident;
  if (!ICAOS.includes(icao)) continue;
  airports[icao] = {
    icao,
    iata: row.iata_code,
    name: row.name,
    centerLat: round6(+row.latitude_deg),
    centerLon: round6(+row.longitude_deg),
  };
}

const runways = {};
for (const row of readCsv(resolve(root, "data/runways.csv"))) {
  const ident = row.airport_ident;
  if (!ICAOS.includes(ident) || row.closed === "1") continue;
  const { le_latitude_deg: leLat, le_longitude_deg: leLon, he_latitude_deg: heLat, he_longitude_deg: heLon } = row;
  if (!leLat || !leLon || !heLat || !heLon) continue;
  (runways[ident] ??= []).push({
    leIdent: row.le_ident,
    heIdent: row.he_ident,
    le: [round6(+leLat), round6(+leLon)],
    he: [round6(+heLat), round6(+heLon)],
    widthFt: +row.width_ft || 100,
  });
}

for (const icao of ICAOS) {
  if (!airports[icao]) throw new Error(`Missing airport row for ${icao}`);
  if (!runways[icao]?.length) throw new Error(`Missing runways for ${icao}`);
}

const catalog = Object.fromEntries(
  ICAOS.map((icao) => {
    const ap = airports[icao];
    return [
      icao,
      {
        ...ap,
        label: `${ap.iata} — ${ap.name}`,
        runways: runways[icao],
      },
    ];
  }),
);

const out = `// Generated by scripts/generate-airports.mjs — do not edit by hand.
// Source: data/airports.csv + data/runways.csv (OurAirports).

export interface Runway {
  leIdent: string;
  heIdent: string;
  le: [number, number];
  he: [number, number];
  widthFt: number;
}

export interface AirportCatalogEntry {
  icao: string;
  iata: string;
  name: string;
  /** Short label for the control panel. */
  label: string;
  centerLat: number;
  centerLon: number;
  runways: Runway[];
}

export interface AirportGroup {
  label: string;
  icaos: string[];
}

export const DEFAULT_AIRPORT_ICAO = "OMDB";

export const AIRPORT_GROUPS: AirportGroup[] = ${JSON.stringify(GROUPS, null, 2)};

export const AIRPORT_CATALOG: Record<string, AirportCatalogEntry> = ${JSON.stringify(catalog, null, 2)};
`;

writeFileSync(resolve(root, "shared/src/airports.ts"), out);
console.log(`Wrote shared/src/airports.ts (${ICAOS.length} airports)`);
